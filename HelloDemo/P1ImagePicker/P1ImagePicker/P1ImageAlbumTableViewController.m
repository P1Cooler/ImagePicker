- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.albumList.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:kP1ImageAlbumTableViewCellIdentify];
    
    if (!cell) {
        cell = [[UITableViewCell alloc] init];
    }    
    // Configure the cell...
    P1AlbumInfo *albumInfo = self.albumList[indexPath.row];
    cell.textLabel.text = albumInfo.collection.localizedTitle;
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if (self.cellClickBlock) {
        self.cellClickBlock(indexPath);
    }
}

